#include "../includes/push_swap.h"

int		ft_isspace(int c)
{
	char	c1;

	c1 = (char)c;
	if (c1 == '\t' || c1 == '\n' || c1 == '\v' ||
		c1 == '\f' || c1 == ' ' || c1 == '\r')
		return (1);
	else
		return (0);
}
